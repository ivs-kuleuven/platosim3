def Random_Points_in_Polygon(polygon, number):
    """Generate random point within a polygon.
    """
    from shapely.geometry import Point, Polygon

    points = []
    minx, miny, maxx, maxy = polygon.bounds
    while len(points) < number:
        pnt = Point(np.random.uniform(minx, maxx), np.random.uniform(miny, maxy))
        if polygon.contains(pnt):
            points.append(pnt) 
            
    return points


def IS_gDor(alpha=2):
    """Fetch gDor instability strip from Dupret+2004 (Teff vs. L).
    """    
    if alpha == 2:
        # alpha_MLT=2
        IS_lower = np.array([[3.8351, 0.525], [3.831, 0.75833], [3.821, 1.2], [3.819, 1.35]]).T
        IS_upper = np.array([[3.8275, 1.35], [3.8384, 1.25], [3.851, 1.075], [3.875, 0.775],]).T
    elif alpha == 1.5:
        # alpha_MLT=1.5
        IS_lower = np.array([[3.802, 1.200], [3.801, 1.095], [3.812, 0.695]]).T
        IS_upper = np.array([[3.851, 0.6865], [3.8166, 1.146], [3.809, 1.2]]).T
    elif alpha == 1.0:
        # alpha_MLT=1.0
        IS_lower = np.array([[3.77, 0.983], [3.77, 0.862], [3.777, 0.732]]).T
        IS_upper = np.array([[3.801, 0.783], [3.786, 0.921], [3.777, 0.983]]).T
    Teff_low, Teff_upp = 10**IS_lower[0], 10**IS_upper[0]
    logL_low, logL_upp = IS_lower[1], IS_upper[1]

    return Teff_low, Teff_upp, logL_low, logL_upp
    
    
