def draw_normal_Z(ds, rng, sigma=2, limit=-2):
    """Draw Z from normal distribution if any value is below -2.0.
    """
    mean = ds.Z.mean()
    sigm = sigma*ds.Z.std()
    dex  = ds[ds.Z < limit].index
    ds.Z.iloc[dex] = rng.normal(mean, sigm, len(dex))
    dex = ds[ds.Z < limit].index
    ds.Z.loc[dex] = 0
    return ds




