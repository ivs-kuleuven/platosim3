def plot_HRD(fig, ax, x, y, c, clab, anchor, cmap='viridis', width="50%", height="3%",
             log=False, vmin=None, vmax=None, ticks=None):
    
    im = ax.scatter(x, y, c=c, s=20, vmin=vmin, vmax=vmax,
                    cmap=cmap, edgecolors='lightgray')
    axins = inset_axes(ax, width=width, height=height, loc="lower left",
                       bbox_to_anchor=anchor, bbox_transform=ax.transAxes)
    cbar = fig.colorbar(im, cax=axins, extend='both', orientation='horizontal',
                        ticks=ticks)
    cbar.set_label(clab, labelpad=3)                                    
    cbar.ax.tick_params()
    cbar.ax.invert_xaxis()
    tick_locator = ticker.MaxNLocator(nbins=5)
    cbar.locator = tick_locator
    cbar.update_ticks()
    if log:
        ax.set_xlabel(r'$\log T_{\rm eff}$ [K]')
    else:
        ax.set_xlabel(r'$T_{\rm eff}$ [K]')
    ax.set_ylabel(r'log $L / L_{\odot}$')
    ax.invert_xaxis()




def get_weight(w, df, ncams=6, N=1500, max_mag=17, max_weight=0.1):   
    """Function to use determine histogram weights to select each camera group.
    """

    # Magnitude range (is 1 element longer)
    mag_hist = w[1][:-1]

    # Weight of histogram (plus custom factor)
    w_hist = 1 / w[0]

    # Replace inf with offset
    w_hist[w_hist == np.inf] = w_hist.min()

    # Normalized weights
    wn_hist = w_hist / w_hist.max()

    # If requested, suppress faint regime
    dex = ut.findNearestIndex(mag_hist, max_mag)
    wn_hist[dex:] = max_weight
        

    # Interpolate weights over magnitude grid
    interpolate = scipy.interpolate.make_interp_spline(mag_hist, wn_hist, k=1)

    # Magnitude grid for interpolation
    mag = np.linspace(mag_hist[0], mag_hist[-1], df[df.ncams == ncams].shape[0])

    # Calculate interpolated weights
    return mag, interpolate(mag)



