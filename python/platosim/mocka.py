def CaMD_ellipse(df, star):
    """Function to select all points enclosed within an ellipse.
    """
    cos_angle = np.cos(np.radians(180-star[3]))
    sin_angle = np.sin(np.radians(180-star[3]))
    xc = df.BP_RP - star[0][0]
    yc = df.Mg    - star[0][1]
    xct = xc * cos_angle - yc * sin_angle
    yct = xc * sin_angle + yc * cos_angle 
    rad_cc = (xct**2/(star[1]/2.)**2) + (yct**2/(star[2]/2.)**2)
    indices = np.where(rad_cc <= 1)[0]
    return df.loc[indices]



def spectype_density(col1, df0, idir, cut=25):  
    """Function to generate density maps for each spectral type.
    """ 
    print(f'Generating density map for: {col1}')
    spectype = np.array(['O', 'B', 'A', 'F', 'G', 'K', 'M', 'unknown', 'CSTAR'])
    
    # Generate catalogue for each spectral type
    for i in range(len(spectype)):
        
        # Select each spectral type
        dx = df0[df0.spec == spectype[i]]
        
        # Select cuts to manage dataset
        if spectype[i] in ['A', 'F', 'G', 'K']:
            xx = int(1 + dx.shape[0]/df0.shape[0] * 100)
        else:
            xx = 1
        uu = xx/100 + (cut-1)/100
        print(f'{spectype[i]}: Plot step: {xx}; Density cut: {int(uu*100)} %')
        
        # Fetch densities
        dx = dx.iloc[::xx]
        x, y, z, dex = ut.sortAfterDensity(dx[col1].to_numpy(), dx.BP_RP.to_numpy())
        
        # Remove less dense regions
        dx0 = pd.DataFrame()
        dx0[col1]      = x
        dx0['BP_RP']   = y
        dx0['density'] = z/z.max()
        dx0['dex']     = dex
        dx0['logg']    = dx.logg
        dx0['R']       = dx.R
        dx0['M']       = dx.M
        dx0['L']       = dx.L
        dx0 = dx0[dx0.density > uu]
        
        # Save df to feather
        dx0.reset_index(drop=True, inplace=True)
        dx0.to_feather(f'{idir}/starcat_GaiaDR3_{col1}_SpecType_{spectype[i]}.ftr')
        
        
    
