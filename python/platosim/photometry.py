class Photometry(object):

    """Class for PlatoSim photometry and time series analysis.
    
    This class provides the Python interface to an Feather file generated
    by the PLATO Photometric Pipeline that comes with the PlatoSim software.

    Usage example for a single feather file:
        >>> lc1 = Photometry("000000001_Ncam1.1_Q1.ftr")

    Usage example for a batch of feather files:
        >>> phot = Photometry("</path/to/files>", "multi")
    """

    """Class that provides the Python interface to an HDF5 file generated by PlatoSim.

    Usage example:
        >>> f = SimFile("output.hdf5")
    """


    def __init__(self, filename):

        """Open the HDF5 file.

        INPUT  : filename: string containing the full path of the HDF5 file
        OUTPUT : None
        """
        
        self.filename = filename
        self.hdf5file = h5py.File(filename, "r")

        # Check that photometry has been saved

        if "Photometry" not in self.hdf5file["/"].keys():
            ut.errorcode("error", "No photometry present in the HDF5 file!")




    def __del__(self):
        """
        PURPOSE: destructor
        """

        pass



    
    def reload(self):
        """
        Close the file, and reload it. 
        Useful when the file may have changed meanwhile.
        """

        self.hdf5file.close()
        self.hdf5file = h5py.File(self.filename, "r")
        return


    
