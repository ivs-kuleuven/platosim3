    def __init__(self, filename):

        """Open the HDF5 file.

        INPUT  : filename: string containing the full path of the HDF5 file
        OUTPUT : None
        """
        
        self.filename = filename
        self.hdf5file = h5py.File(filename, "r")

        # Check that photometry has been saved

        if "Photometry" not in self.hdf5file["/"].keys():
            ut.errorcode("error", "No photometry present in the HDF5 file!")




    def __del__(self):
        """
        PURPOSE: destructor
        """

        pass



    
    def reload(self):
        """
        Close the file, and reload it. 
        Useful when the file may have changed meanwhile.
        """

        self.hdf5file.close()
        self.hdf5file = h5py.File(self.filename, "r")
        return


    #--------------------------------------------------------------#
    #                          PREPARE DATA                        #
    #--------------------------------------------------------------#


    def files(self, extension="zip", path=None):
        """
        PURPOSE: Function to unpack all files multi-camera and multi-quarter
                 light curves simulated for a single star.
        """

        # Set path to files as default
        if path is None: path = self.path

        # Fetch all zip files
        files = glob.glob(f"{path}/*.{extension}")

        return files

    
    

    def unpack(self, files=None):
        """
        PURPOSE: Function to unpack all files multi-camera and multi-quarter
                 light curves simulated for a single star.
        """

        # Fetch all zip files
        if files is None:
            files = glob.glob(self.path + "/*.zip")

        # Unpack zip files
        for f in files:
            with ZipFile(f, 'r') as unzip:
                unzip.extractall(self.path)


    
