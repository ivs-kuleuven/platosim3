#!/usr/bin/env python3

"""
This PlatoSim class is designed to ease the time series analysis.
The class can be applied to both the HDF5 output files produced
by PlatoSim or the Feather output files generated by PLATOnium
while is use the L1 proto-pipeline to extract the photometry.
"""

import os
import sys
import pathlib
import argparse
import numpy as np
import pandas as pd
from pathlib import Path
from platosim.utilities import errorcode
from platosim.lightcurve import LightCurve
from transitleastsquares import transitleastsquares

#==============================================================#
#               PARSING COMMAND-LINE ARGUMENTS                 #
#==============================================================#

parser = argparse.ArgumentParser(epilog=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)

man_group = parser.add_argument_group('MANDATORY PARAMETERS')
man_group.add_argument('ifile', type=str, help='Input file df')

out_group = parser.add_argument_group('I/O PARAMETERS')
out_group.add_argument('-o', '--odir', type=str, help='Output directory (default is ifile location')
out_group.add_argument('--tmax',       type=float, help='Max duration to shorten time series')
out_group.add_argument('--threads',    type=float, help='Number of threads to be used')

args = parser.parse_args()
ifile = args.ifile
odir  = args.odir
tmax  = args.tmax
threads = args.threads

#--------------------------

# Resolve path
if odir:
    odir = Path(odir).resolve()
    try: os.mkdir(odir)
    except: pass
else:
    odir = Path(ifile).parents[0]

# Load file
df = pd.read_feather(ifile)

# Shorten time series if needed
if tmax:
    df = df[df.time < tmax * day]

if not threads:
    threads = 10

# Perform TLS vetting with script outside this notebook to use more CPUs
model = transitleastsquares(df.time, df.flux)
results = model.power(R_star=1.0,
                      R_star_min=0.13,
                      R_star_max=3.50,
                      M_star=1.0,
                      M_star_min=0.1,
                      M_star_max=1.0,
                      period_min=10.0,
                      period_max=400.0,
                      n_transits_min=2,
                      use_threads=int(threads))

# Stop script if no planet is found
if results.period is np.nan:
    print('Vetting failed: No planets found..')
    exit()

# Save stdout
with open(odir / 'tls_stdout.txt', 'w') as f:
    print('Period', format(results.period, '.5f'), 'd', file=f)
    print('Transit times                     : ', format(results.transit_times), file=f)
    print('Transit depth                     : ', format(results.depth, '.5f'), file=f) 
    print('Best duration (days)              : ', format(results.duration, '.5f'), file=f)
    print('Signal detection efficiency (SDE) : ', results.SDE, file=f)

# Save model
phase     = results.model_folded_phase
flux      = (results.model_folded_model-1)*1e6
df_model  = pd.DataFrame({'phase':phase, 'flux':flux})
df_model.to_feather(odir / 'tls_model.ftr')

# Save model
period   = results.periods
power    = results.power
df_power = pd.DataFrame({'period':period, 'SDE':power})
df_power.to_feather(odir / 'tls_power.ftr')


