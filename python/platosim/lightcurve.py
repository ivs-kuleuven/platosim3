class LightCurve(object):
    """Class for PlatoSim photometry and time series analysis.
    
    This class provides the Python interface to the output files generated
    by the PlatoSim toolkit PLATOnium that comes with the PlatoSim software.

    Usage example for a single HDF5/feather file:
        >> lc = LightCurve("000000001_Ncam1.1_Q1.ftr")

    Usage example for a batch of HDF5/feather files:
        >> lcs = LightCurve("</path/to/files>", "multi")
    """

    def __init__(self, filename, mode="single", base='e/s', path=False):

        # Constants
        self.pixelSize = 18  # [micron]
        
        # Matplotlib
        self.aa = 0.5  # Alpha
        self.ms = 3    # MarkerSize
        self.lw = 2    # LineWidth

        # Read either a single file or multiple files
        self.mode = mode
        self.base = base
        if path:
            self.path = Path(path)
        else:
            self.path = path
        
        # Check if data frame or file path
        self.fileExtention = None
        if isinstance(filename, pd.DataFrame):
            self.df = filename    
        else:
            self.df = None
            self.filename = Path(filename)
            
            # Check if file or directory
            if self.filename.is_file():
                self.path = self.filename.parents[0]
                self.fileExtention = Path(filename).suffix
            elif self.filename.is_dir():
                self.path = Path(filename)
                
        # Common parameters (depding on mode)
        self.mask_updates = np.array([])

                
        # SINGLE-CAMERA SIMULATION

        if mode == "single":
            
            # Check if feather of HDF5 output file

            if self.fileExtention == ".ftr":

                # Simply load file
                self.df = pd.read_feather(filename)

                # Drop some columns
                if 'chi2' in self.df:
                    self.df.drop(columns=['chi2', 'iter', 'lamb'], inplace=True)

            elif self.fileExtention == ".hdf5":

                # L1A pipeline data product
                if Path(filename).stem[22:] == 'LIGHTCURVE_L1A_IMAGETTE':
                    f = h5py.File(filename, "r")
                    self.df = pd.DataFrame({'time': f['FLUX_TS']['ONBOARD_TIME'],
                                            'flux': f['FLUX_TS']['FLUX'],
                                            'flag': f['FLUX_TS']['IMAGETTE_OUTLIERS_NB']})
                    # Remove flagged outliers
                    self.df = self.df[self.df.flag == 0]
                    
                else:
                    # Load HDF5 file
                    simfile = SimFile(filename)
                    # Fetch light curve
                    self.df = simfile.getLightCurve(1, df=True)
                    # Add time column if not found
                    if not 'time' in self.df:
                        exptime  = simfile.getExposureTime()
                        readtime = simfile.getReadoutTime()
                    # Mask and updates 
                    self.mask_updates  = simfile.getMaskUpdateEvents()
                    self.mask_aperture = simfile.getApertureMask(starID=1)

            # Option to load CSV file
                
            elif self.fileExtention == ".txt":
                self.df = pd.read_csv(filename, sep=' ', names=['time', 'dmag'])
                
            else:
                errorcode("error", "File should be in the format of .ftr or .hdf5!")

                
        # MULTI-CAMERA SIMULATIONS
            
        elif mode == "multi":
    
            # No actions so far
            pass


        # FINAL LC MODE
            
        elif mode == 'final':

            # Simply load file
            self.df = pd.read_feather(filename)
                        
            
        # COMMON ACTIONS

        # Cadence/cycle time (exposure + readout time)
        if self.df is not None:
            self.cadence = self.df.time.diff().iloc[1]

            
