        # Plot quarters

        quarters = np.arange(0, time[-1], 90)
        for Q in quarters:
            if Q == 0:
                ax.axvline(x=Q, c='darkgray', linestyle='-.', linewidth=1, label='Quarter marks')
            else:
                ax.axvline(x=Q, c='darkgray', linestyle='-.', linewidth=1)

    
    


