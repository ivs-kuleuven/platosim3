                        #if rcen_err_mean > 100: rcen_err_mean = 100

                        # Mean flux error in percent

                        flux     = np.abs(lc.flux())
                        flux_err = np.abs(lc.flux_err())
                        flux_err_mean = flux_err.mean() / flux.mean() * 100                
                        #if flux_err_mean > 100: flux_err_mean = 100

