        ax.set_xlim(self.time_limit(quarters))
        ax.set_xlabel(f'Time [days]')
        ax.set_ylabel(r"Flux [ke$^-$ s$^{-1}$]")
        ax.set_xlim(self.time_limit(quarters))
        plt.tight_layout()
        
        return fig, ax


    

    
