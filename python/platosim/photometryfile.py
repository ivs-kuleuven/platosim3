    def bin(self, binsize=144, cadence=25, unit="e/s"):
        """
        PURPOSE: Calculates the Median Absolute Deviation of the light curve.
        """

        time = self.time()
        flux = self.flux(unit=unit)
        
        nbins = np.floor(len(time) / binsize).astype(int)
        tbins = round((binsize * cadence) / 3600., 2)
        
        time_bin = np.array([time[i*binsize:(i+1)*binsize].mean() for i in range(nbins)])
        flux_bin = np.array([flux[i*binsize:(i+1)*binsize].mean() for i in range(nbins)])

        return time_bin, flux_bin, tbins


    



    def plot(self, time_unit="d", flux_unit="e/s", errorbar=False,
             median_filter=False, binsize=False, figsize=(12,6)):
        """
        PURPOSE: Function normalize the input flux and change time units to days. 

        NOTE: Function tailored to PLATOniums output format feather!

        PARAMETERS
        ----------
        data : pdarray
            Array containing at least a time and flux column (and potential flux_err)
        flux_err: boolen
           Wheather or not a flux_err column is present and should thus be added.

        RETURN
        ------
        data
        """

        # Label for flux
        if   flux_unit == "e/s":  ylab = r"Flux [e$^-$ s$^{-1}$]"
        elif flux_unit == "norm": ylab = r"Flux normalized"
        elif flux_unit == "ppt":  ylab = r"Flux [ppt]"
        elif flux_unit == "ppm":  ylab = r"Flux [ppm]"
        else: ut.errorcode("error", "No such flux unit!")

        # Fetch columns
        time = self.time(unit=time_unit)
        flux = self.flux(unit=flux_unit)
        flux_err = self.flux_err(unit=flux_unit)
        
        # Create matplotlib object 
        fig, ax = plt.subplots(1, 1, figsize=figsize)
        
        # Plot the input variable source
        if errorbar:
            ax.errorbar(time, flux, yerr=flux_err, fmt=".", color='k',
                        ecolor='darkgray', elinewidth=1, capsize=0, alpha=0.5,
                        label=f"N-CAM {self.group}.{self.camera} Q{self.quarter}", zorder=1)
        else:
            ax.plot(time, flux, 'k.', ms=self.ms, alpha=self.aa,
                    label=f"N-CAM {self.group}.{self.camera} Q{self.quarter}", zorder=1)

        # Plot a median filter
        if median_filter:
            flux_med = self.fluxMedian(unit=flux_unit)
            ax.plot(time, flux_med, 'b-', lw=self.lw, label='1h median', zorder=2)

        # Show binned mean points if requested
        if binsize:
            time_bin, flux_bin, tbins = self.bin(binsize=binsize, unit=flux_unit)
            ax.plot(time_bin, flux_bin, 'ro', ms=10, mec='k', label=f'{tbins}h bins', zorder=3)
            
        # Settings
        ax.set_xlim(time.iloc[0], time.iloc[-1])
        ax.set_xlabel('Time - BOL [days]')
        ax.set_ylabel(ylab)
        ax.legend(loc='best')
        
        return fig, ax
    




