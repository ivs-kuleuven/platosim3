
#include "Camera.h"










/**
 * \brief      Constructor
 *
 * \param configParam   Configuration parameters for the Camera
 * \param hdf5file      HFD5 file to write the camera information to
 * \param telescope     Telescope on which the camera is mounted on
 * \param sky           Sky object to query which stars we are seeing
 */

Camera::Camera(ConfigurationParameters &configParam, HDF5File &hdf5file, Platform &platform, Telescope &telescope, Sky &sky)
: HDF5Writer(hdf5file), platform(platform), telescope(telescope), sky(sky), focalLength(nullptr), focalPlaneAngle(nullptr),
  distortionCoef(nullptr), inverseDistortionCoef(nullptr), internalTime(0.0), includeFieldDistortion(true),
  userGivenSkyBackground(-1.0), fluxOfV0Star(0.0)
{

    // Parse the parameters from the configuration file.

    configure(configParam);

    // Create the groups in the HDF5 file where the different arrays generated by Camera will be saved.

    initHDF5Groups();

    // Get the equatorial sky coordinates of the Sun, which is know by platform since it's pointing its sunshield towards it.

    tie(raSun, decSun) = platform.getRADecSun();
}









/**
 * \brief  Destructor, free memory for psf
 */

Camera::~Camera()
{
    flushOutput();
    delete focalLength;
    delete focalPlaneAngle;

    // if includeDistortion was set to false, the following parameters were never
    // allocated, but the were initialised to null pointers in the constructor,
    // so it always OK to delete them.

    delete distortionCoef;
    delete inverseDistortionCoef;
}













/**
 * \brief Creates the group(s) in the HDF5 file where the star information will be stored.
 *        These group(s) have to be created once, at the very beginning.
 */

void Camera::initHDF5Groups()
{
    Log.debug("Camera: initialising HDF5 groups");
    
    hdf5File.createGroup("/StarPositions");
    hdf5File.createGroup("/Background");
    if (writeTransmissionEfficiency)
    {
      hdf5File.createGroup("/TransmissionEfficiency");
    }

    hdf5File.createGroup("/PointLikeGhostPositions");
    hdf5File.createGroup("/ExtendedGhostPositions");
}








/**
 * \brief Collect and returns the IDs of all stars that fall on the sub-field or produce a ghost
 *        that falls on the sub-field (if ghosts are to be included in the simulation).
 *
 * \details Note that this method pulls the detected stars from a map that is filled by
 *          exposeDetector for each exposure. So, depending on when this method is called,
 *          the returned set might be empty or incomplete.
 *
 * \return Set of unique IDs for stars that fall on the sub-field or produce a ghost on the
 *         sub-field (if ghosts are to be included in the simulation).
 */
set<unsigned int> Camera::getAllStarIDs()
{
    set<unsigned int> allStarIDs;            // A set<> stores only unique members

    // Stars on the sub-field

    for(auto timeMapPair: detectedStarInfo)
    {
        for (auto idArrayPair: timeMapPair.second)
        {
            allStarIDs.insert(idArrayPair.first);
        }
    }

    // Originators of extended ghosts on the sub-field

    for (auto timeMapPair: detectedExtendedGhostInfo)
    {
        for (auto idArrayPair: timeMapPair.second)
        {
            allStarIDs.insert(idArrayPair.first);
        }
    }

    // Originators of symmetric point-like ghosts on the sub-field

    for (auto timeMapPair: detectedPointLikeGhostInfo)
    {
        for (auto idArrayPair: timeMapPair.second)
        {
            allStarIDs.insert(idArrayPair.first);
        }
    }

    return allStarIDs;

}






/**
 * \brief Return the mean position on the CCD, and flux of the star with given ID for the last exposure.
 *
 * \details Mean refers to the mean position during the jittering during the exposure. If the star ID
 *          is not found in the list, the value -1 is returned for each quantity.
 *
 * \return A tuple with the following quantities:
 *    timeStamp: the time stamp of the last exposure                              [s]
 *    xFPmean: mean (during the exposure) focal plane x-coordinate of the star    [mm]
 *    yFPmean: mean focal plane y-coordinate of the star                          [mm]
 *    rowPixMean: mean subfield (fractional) row coordinate                       [pix]
 *    colPixMean: mean subfield (fractional) column coordinate                    [pix]
 *    sumFlux: the integrated flux of the star during one exposure                [photons/exposure]
 */

tuple<double, double, double, double, double, double> Camera::getInfoForTheMostRecentExposureForStar(int starID)
{
    // The detected star info is stored in a map as:   map<double, map<unsigned int, array<double, 6>>> detectedStarInfo;
    // so that  detectedStarInfo[startTime][starID] contains the values (xFPsum, yFPsum, rowPixSum, colPixSum, sumFlux, Ndetections)
    // In C++ maps are sorted on key values (in ascending order). To find the time stamp of the most recent exposure,
    // we simply need to access the last element. rbegin() is a reverse-iterator, starting with the last element

    double timeStamp = detectedStarInfo.rbegin()->first;

    // Find the map item with the given starID

    auto it = detectedStarInfo[timeStamp].find(starID);
    if (it == detectedStarInfo[timeStamp].end())
    {
        // The starID couldn't be found in the map.

        return make_tuple(-1, -1, -1, -1, -1, -1);
    }
    else
    {
        double xFPmean    = detectedStarInfo[timeStamp][starID][0] / detectedStarInfo[timeStamp][starID][5];
        double yFPmean    = detectedStarInfo[timeStamp][starID][1] / detectedStarInfo[timeStamp][starID][5];
        double rowPixMean = detectedStarInfo[timeStamp][starID][2] / detectedStarInfo[timeStamp][starID][5];
        double colPixMean = detectedStarInfo[timeStamp][starID][3] / detectedStarInfo[timeStamp][starID][5];
        double sumFlux    = detectedStarInfo[timeStamp][starID][4];

        return make_tuple(timeStamp, xFPmean, yFPmean, rowPixMean, colPixMean, sumFlux);
    }

}






/**
 *  \brief return the begin and end iterator of the star info of detectedStarInfo[most_recent_time];
 */

pair<starInfoIterator, starInfoIterator> Camera::getInfoForTheMostRecentExposureForAllStars()
{
   // The detected star info is stored in a map as:   map<double, map<unsigned int, array<double, 6>>> detectedStarInfo;
    // so that  detectedStarInfo[startTime][starID] contains all the information.
    // In C++ maps are sorted on key values (in ascending order). To find the time stamp of the most recent exposure,
    // we simply need to access the last element. rbegin() is a reverse-iterator, starting with the last element

    double timeStamp = detectedStarInfo.rbegin()->first;
    return make_pair(detectedStarInfo[timeStamp].begin(), detectedStarInfo[timeStamp].end());
}




/**
 * \brief Write all recorded information to the HDF5 output file
 *
 */

void Camera::flushOutput()
{

    // Extract and save the time points of all exposures
    // Note: keyValuePair is (key, value) pair, where key is also a pair consisting of the startTime and StarID

    if (writeStarPositions)
    {
        Log.info("Camera: writing star positions to HDF5 file");

        vector<double> time;
        for(auto keyValuePair: detectedStarInfo) time.push_back(keyValuePair.first);
        if (!time.empty())
        {
            hdf5File.writeArray("StarPositions/", "Time", time.data(), time.size());
        }
        else
        {
            Log.warning("Camera: No star positions to write to HDF5 file.");
        }

        // For each of the exposures, make a subgroup and write the position and flux of all detected stars.
        // Because some stars at the edge may jitter in and out of the subfield from one exposure to the other,
        // the written arrays may not be equally long for each exposure.

        for (int n = 0; n < time.size(); n++)
        {
            // Make the sub-group

            stringstream myStream;
            myStream << "Exposure" << setfill('0') << setw(6) << beginExposureNr + n;
            const string exposureGroupName = "/StarPositions/" + myStream.str();
            hdf5File.createGroup(exposureGroupName);

            // Collect the different time series. For the positions, we only compute the sum, so we still need
            // to divide by N to compute the average, where N is the number of times the star was detected to be
            // in the subfield during an exposure.

            vector<unsigned int> starIDs;
            vector<double> xFPmm;
            vector<double> yFPmm;
            vector<double> rowPix;
            vector<double> colPix;
            vector<double> flux;

            for(auto keyValuePair: detectedStarInfo[time[n]])
            {
                const unsigned int starID = keyValuePair.first;
                starIDs.push_back(starID);                       // list of starIDs for this exposure only
                xFPmm.push_back(detectedStarInfo[time[n]][starID][0] / detectedStarInfo[time[n]][starID][5]);
                yFPmm.push_back(detectedStarInfo[time[n]][starID][1] / detectedStarInfo[time[n]][starID][5]);
                rowPix.push_back(detectedStarInfo[time[n]][starID][2] / detectedStarInfo[time[n]][starID][5]);
                colPix.push_back(detectedStarInfo[time[n]][starID][3] / detectedStarInfo[time[n]][starID][5]);
                flux.push_back(detectedStarInfo[time[n]][starID][4]);
            }

            // Write the time series to HDF5

            if(!starIDs.empty())
            {
                hdf5File.writeArray(exposureGroupName, "starID", starIDs.data(), starIDs.size());
                hdf5File.writeArray(exposureGroupName, "xFPmm",  xFPmm.data(),   xFPmm.size());
                hdf5File.writeArray(exposureGroupName, "yFPmm",  yFPmm.data(),   yFPmm.size());
                hdf5File.writeArray(exposureGroupName, "rowPix", rowPix.data(),  rowPix.size());
                hdf5File.writeArray(exposureGroupName, "colPix", colPix.data(),  colPix.size());
                hdf5File.writeArray(exposureGroupName, "flux",   flux.data(),    flux.size());
            }
        }
    }
    else 
    {
        Log.warning("Camera: No star positions written to HDF5 file by user demand (see input file).");
    }


    // If the ghost option was set, also write for each exposure the info of the ghost stars to the HDF5 file.

    if (includeGhosts && writeGhostPositions)
    {
        Log.info("Camera: writing pointlike ghost positions to HDF5 file");

        vector<double> time;
        for(auto keyValuePair: detectedPointLikeGhostInfo) time.push_back(keyValuePair.first);
        if (!time.empty())
        {
            hdf5File.writeArray("PointLikeGhostPositions/", "Time", time.data(), time.size());
        }
        else
        {
            Log.warning("Camera: No point-like ghost positions to write to HDF5 file.");
        }

        for (int n = 0; n < time.size(); n++)
        {
            stringstream myStream;
            myStream << "Exposure" << setfill('0') << setw(6) << beginExposureNr + n;

            // Write the info for the point-like ghost star positions to HDF5

            vector<unsigned int> starIDs;
            vector<double> xFPmm;
            vector<double> yFPmm;
            vector<double> rowPix;
            vector<double> colPix;
            vector<double> flux;
            vector<double> ghostRadius;

            for(auto keyValuePair: detectedPointLikeGhostInfo[time[n]])
            {
                const unsigned int starID = keyValuePair.first;
                starIDs.push_back(starID);                       // list of starIDs for this exposure only
                xFPmm.push_back(detectedPointLikeGhostInfo[time[n]][starID][0] / detectedPointLikeGhostInfo[time[n]][starID][5]);
                yFPmm.push_back(detectedPointLikeGhostInfo[time[n]][starID][1] / detectedPointLikeGhostInfo[time[n]][starID][5]);
                rowPix.push_back(detectedPointLikeGhostInfo[time[n]][starID][2] / detectedPointLikeGhostInfo[time[n]][starID][5]);
                colPix.push_back(detectedPointLikeGhostInfo[time[n]][starID][3] / detectedPointLikeGhostInfo[time[n]][starID][5]);
                flux.push_back(detectedPointLikeGhostInfo[time[n]][starID][4]);
            }

            const string pointLikeGhostGroupName = "/PointLikeGhostPositions/" + myStream.str();
            hdf5File.createGroup(pointLikeGhostGroupName);

            if(!starIDs.empty())
            {
                hdf5File.writeArray(pointLikeGhostGroupName, "starID", starIDs.data(), starIDs.size());
                hdf5File.writeArray(pointLikeGhostGroupName, "xFPmm",  xFPmm.data(),   xFPmm.size());
                hdf5File.writeArray(pointLikeGhostGroupName, "yFPmm",  yFPmm.data(),   yFPmm.size());
                hdf5File.writeArray(pointLikeGhostGroupName, "rowPix", rowPix.data(),  rowPix.size());
                hdf5File.writeArray(pointLikeGhostGroupName, "colPix", colPix.data(),  colPix.size());
                hdf5File.writeArray(pointLikeGhostGroupName, "flux",   flux.data(),    flux.size());
            }


            // Write the info for the extended ghost star positions to HDF5

            Log.info("Camera: writing extended ghost positions to HDF5 file");

            starIDs.clear();
            xFPmm.clear();
            yFPmm.clear();
            rowPix.clear();
            colPix.clear();
            flux.clear();
            ghostRadius.clear();
        }

        time.clear();
        for(auto keyValuePair: detectedExtendedGhostInfo) time.push_back(keyValuePair.first);
        if (!time.empty())
        {
            hdf5File.writeArray("ExtendedGhostPositions/", "Time", time.data(), time.size());
        }
        else
        {
            Log.warning("Camera: No extended ghost positions to write to HDF5 file.");
        }

        for (int n = 0; n < time.size(); n++)
        {
            stringstream myStream;
            myStream << "Exposure" << setfill('0') << setw(6) << beginExposureNr + n;


            // Write the info for the extended ghost star positions to HDF5

            vector<unsigned int> starIDs;
            vector<double> xFPmm;
            vector<double> yFPmm;
            vector<double> rowPix;
            vector<double> colPix;
            vector<double> flux;
            vector<double> ghostRadius;


            for(auto keyValuePair: detectedExtendedGhostInfo[time[n]])
            {
                const unsigned int starID = keyValuePair.first;
                starIDs.push_back(starID);                       // list of starIDs for this exposure only
                xFPmm.push_back(detectedExtendedGhostInfo[time[n]][starID][0] / detectedExtendedGhostInfo[time[n]][starID][5]);
                yFPmm.push_back(detectedExtendedGhostInfo[time[n]][starID][1] / detectedExtendedGhostInfo[time[n]][starID][5]);
                rowPix.push_back(detectedExtendedGhostInfo[time[n]][starID][2] / detectedExtendedGhostInfo[time[n]][starID][5]);
                colPix.push_back(detectedExtendedGhostInfo[time[n]][starID][3] / detectedExtendedGhostInfo[time[n]][starID][5]);
                flux.push_back(detectedExtendedGhostInfo[time[n]][starID][4]);
                ghostRadius.push_back(detectedExtendedGhostInfo[time[n]][starID][6] / detectedExtendedGhostInfo[time[n]][starID][5]);
            }
        
            const string extendedGhostGroupName = "/ExtendedGhostPositions/" + myStream.str();
            hdf5File.createGroup(extendedGhostGroupName);
        
            if(!starIDs.empty())
            {
                hdf5File.writeArray(extendedGhostGroupName, "starID", starIDs.data(), starIDs.size());
                hdf5File.writeArray(extendedGhostGroupName, "xFPmm",  xFPmm.data(),   xFPmm.size());
                hdf5File.writeArray(extendedGhostGroupName, "yFPmm",  yFPmm.data(),   yFPmm.size());
                hdf5File.writeArray(extendedGhostGroupName, "rowPix", rowPix.data(),  rowPix.size());
                hdf5File.writeArray(extendedGhostGroupName, "colPix", colPix.data(),  colPix.size());
                hdf5File.writeArray(extendedGhostGroupName, "flux",   flux.data(),    flux.size());
                hdf5File.writeArray(extendedGhostGroupName, "radius", ghostRadius.data(), ghostRadius.size());
            }
        }
    } 

    

    // Write the total sky background flux values [photons/pixel/exposure] to HDF5 in a custom group

    hdf5File.writeArray("Background/", "skyBackground", skyBackgroundValues.data(), skyBackgroundValues.size());

    // Write the transmissionEfficiency values for each exposureTime to HDF5 in a custom group
    if (writeTransmissionEfficiency)
    {
    hdf5File.writeArray("TransmissionEfficiency/", "transmissionEfficiency", transmissionEfficiencyValues.data(), transmissionEfficiencyValues.size());
    }

}












/**
 * \brief Configure the Camera object using the ConfigurationParameters
 *
 * \param configParam: the configuration parameters
 */

void Camera::configure(ConfigurationParameters &configParam)
{
    numExposures           = configParam.getInteger("ObservingParameters/NumExposures");
    beginExposureNr        = configParam.getInteger("ObservingParameters/BeginExposureNr");


    // The focal length can either be a fixed value, or given by a time series in a file

    string focalLengthSource = configParam.getString("Camera/FocalLength/Source");
    if (focalLengthSource == "ConstantValue")
    {
        double focalLengthValue = configParam.getDouble("Camera/FocalLength/ConstantValue") * 1000;     // [m] -> [mm]
        focalLength = new Parameter<double>(focalLengthValue);

        Log.info("Camera: Using a constant focal length: " + to_string(focalLengthValue) + " m");
    }
    else if (focalLengthSource == "FromFile")
    {
        string focalLengthInputFile = configParam.getAbsoluteFilename("Camera/FocalLength/FromFile");
        focalLength = new Parameter<double>(focalLengthInputFile, 1000);                                // [m] -> [mm]

        Log.info("Camera: Reading focal length values from " + focalLengthInputFile);
    }



    // The focal plane orientation can either be a fixed value, or given by a time series in a file

    string focalPlaneAngleSource = configParam.getString("Camera/FocalPlaneOrientation/Source");
    if (focalPlaneAngleSource == "ConstantValue")
    {
        double focalPlaneAngleValue = deg2rad(configParam.getDouble("Camera/FocalPlaneOrientation/ConstantValue")); // [deg]->[rad]
        focalPlaneAngle = new Parameter<double>(focalPlaneAngleValue);                                              // [rad]

        Log.info("Camera: Using a constant focal plane orientation: " + to_string(focalPlaneAngleValue) + " m");
    }
    else if (focalPlaneAngleSource == "FromFile")
    {
        string focalPlaneAngleInputFile = configParam.getAbsoluteFilename("Camera/FocalPlaneOrientation/FromFile");
        focalPlaneAngle = new Parameter<double>(focalPlaneAngleInputFile, deg2rad(1.0));                           // [deg]->[rad]

        Log.info("Camera: Reading focal plane orientation from " + focalPlaneAngleInputFile);
    }



    // The distortion coefficients can either be a fixed value, or given by a time series in a file
    // Idem for the inverse distortion coefficients.

    includeFieldDistortion = configParam.getBoolean("Camera/IncludeFieldDistortion");

    if (includeFieldDistortion)
    {
        distortionModel              = configParam.getString("Camera/FieldDistortion/Type");
        string fieldDistortionSource = configParam.getString("Camera/FieldDistortion/Source");
        if (fieldDistortionSource == "ConstantValue")
        {
            // Read the distortion coefficients from the yaml input file. Currently vector<>s are
            // being read while Parameter<> expects array<>s. So we'll have to do some overhead
            // to convert one to the other.

            vector<double> distortionCoefVector        = configParam.getDoubleVector("Camera/FieldDistortion/ConstantCoefficients");
            vector<double> inverseDistortionCoefVector = configParam.getDoubleVector("Camera/FieldDistortion/ConstantInverseCoefficients");
            array<double, 3> distortionCoefArray, inverseDistortionCoefArray;

            if ((distortionCoefVector.size() != 3) || (inverseDistortionCoefVector.size() != 3))
            {
                string msg = "Camera::configure(): number of (inverse) distortion coefficients in input yaml file != 3.";
                throw ConfigurationException(msg);
            }
            else
            {
                copy(distortionCoefVector.begin(), distortionCoefVector.end(), distortionCoefArray.begin());
                copy(inverseDistortionCoefVector.begin(), inverseDistortionCoefVector.end(), inverseDistortionCoefArray.begin());
            }

            distortionCoef        = new Parameter<double, 3>(distortionCoefArray);
            inverseDistortionCoef = new Parameter<double, 3>(inverseDistortionCoefArray);

            Log.info("Camera: Using a constant distortion law");
        }
        else if (fieldDistortionSource == "FromFile")
        {
            string distortionInputFile = configParam.getAbsoluteFilename("Camera/FieldDistortion/CoefficientsFromFile");
            string invDistortionInputFile = configParam.getAbsoluteFilename("Camera/FieldDistortion/InverseCoefficientsFromFile");
            distortionCoef = new Parameter<double, 3>(distortionInputFile, 1);
            inverseDistortionCoef = new Parameter<double, 3>(invDistortionInputFile, 1);

            Log.info("Camera: Reading distortion coefficients from " + distortionInputFile);
            Log.info("Camera: Reading inverse distortion coefficients from " + invDistortionInputFile);
        }
    }
    else
    {
        Log.info("Camera: Ignoring field distortion");
    }


    // Configure the ghost star parameters 

    includeGhosts = configParam.getBoolean("Camera/IncludeGhosts");

    if(includeGhosts)
    {
        distanceCutOffPointLikeGhosts = deg2rad(configParam.getDouble("Camera/Ghosts/PointLike/DistanceCutOff"));   // [radians]
        fluxRatioOnAxisPointLikeGhosts = configParam.getDouble("Camera/Ghosts/PointLike/FluxRatio") / 100.0;
        distanceRatioExtendedGhosts = configParam.getDouble("Camera/Ghosts/Extended/DistanceRatio");
        fluxRatioExtendedGhosts = configParam.getDouble("Camera/Ghosts/Extended/FluxRatio") / 100.0;

        vector<double> extendedGhostRadiusCoefVector = configParam.getDoubleVector("Camera/Ghosts/Extended/RadiusCoefficients");
        array<double, 3> extendedGhostRadiusCoefArray;
        copy(extendedGhostRadiusCoefVector.begin(), extendedGhostRadiusCoefVector.end(), extendedGhostRadiusCoefArray.begin());

        extendedGhostRadiusCoefficients = new Parameter<double, 3>(extendedGhostRadiusCoefArray);
    }
    else
    {
        Log.info("Camera: Ignoring ghosts");
    }


    plateScale             = configParam.getDouble("Camera/PlateScale");
    throughputBandwidth    = configParam.getDouble("Camera/ThroughputBandwidth");
    throughputLambdaC      = configParam.getDouble("Camera/ThroughputLambdaC");

    includeAberrationCorrection = configParam.getBoolean("Camera/IncludeAberrationCorrection");
    aberrationCorrectionType    = configParam.getString("Camera/AberrationCorrection/Type");


    fluxOfV0Star           = configParam.getDouble("ObservingParameters/Fluxm0");                 // [phot/s/m^2/nm]
    userGivenSkyBackground = configParam.getDouble("Sky/SkyBackground");          // [phot/pix/s]

    writeStarPositions     = configParam.getBoolean("ControlHDF5Content/WriteStarPositions");
    writeGhostPositions    = configParam.getBoolean("ControlHDF5Content/WriteGhostPositions");
    writeTransmissionEfficiency = configParam.getBoolean("ControlHDF5Content/WriteTransmissionEfficiency");
    
}











/**
 * \brief Update the time dependent parameters of the Camera to their
 *        value at the given time point
 *
 * \param time: current time
 *
 * \return
 */

void Camera::updateParameters(double time)
{
    focalLength->updateValue(time);
    focalPlaneAngle->updateValue(time);

    if (includeFieldDistortion)
    {
        distortionCoef->updateValue(time);
        inverseDistortionCoef->updateValue(time);
    }
}










